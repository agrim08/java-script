const day = "monday";

switch (day) {
  case "monday":
    console.log("make schedule for whole week");
    break;
  case "tuesday":
    console.log("JS");
    break;
  case "wednesday":
    console.log("HTML");
    break;
  case "thursday":
    console.log("PYTHON");
    break;
  case "friday":
    console.log("C");
    break;
  case "satday":
    console.log("JAVA");
    break;
  case "sunday":
    console.log("SQL");
    break;
  default:
    console.log("not a valid day!");
}
